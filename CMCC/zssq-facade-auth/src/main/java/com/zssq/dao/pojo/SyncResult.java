package com.zssq.dao.pojo;

import java.io.Serializable;

public class SyncResult implements Serializable {

	private static final long serialVersionUID = -1037579292278099354L;
	private Long id;
	private String succSyncId;
	private String failSyncId;
	private String syncType;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getSuccSyncId() {
		return succSyncId;
	}

	public void setSuccSyncId(String succSyncId) {
		this.succSyncId = succSyncId;
	}

	public String getFailSyncId() {
		return failSyncId;
	}

	public void setFailSyncId(String failSyncId) {
		this.failSyncId = failSyncId;
	}

	public String getSyncType() {
		return syncType;
	}

	public void setSyncType(String syncType) {
		this.syncType = syncType;
	}
}
