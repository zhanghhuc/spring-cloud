package com.zssq.dao.pojo;

import java.io.Serializable;
import java.util.List;

public class SysMenuInfo implements Serializable {

	private static final long serialVersionUID = -6175054922461276466L;
	private Long id;
	private String menuCode;
	private String menuName;
	private String parentCode;
	private Byte menuOrder;
	private String isSel;
	private Byte isEnable;
	private String link;
	private String klass;
	private String active;
	private String state;
	 
	private List<SysMenuInfo> twoLevelMenus;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getMenuCode() {
		return menuCode;
	}

	public void setMenuCode(String menuCode) {
		this.menuCode = menuCode;
	}

	public String getMenuName() {
		return menuName;
	}

	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}

	public String getParentCode() {
		return parentCode;
	}

	public void setParentCode(String parentCode) {
		this.parentCode = parentCode;
	}

	public Byte getMenuOrder() {
		return menuOrder;
	}

	public void setMenuOrder(Byte menuOrder) {
		this.menuOrder = menuOrder;
	}

	public Byte getIsEnable() {
		return isEnable;
	}

	public void setIsEnable(Byte isEnable) {
		this.isEnable = isEnable;
	}

	public String getIsSel() {
		return isSel;
	}

	public void setIsSel(String isSel) {
		this.isSel = isSel;
	}

	public List<SysMenuInfo> getTwoLevelMenus() {
		return twoLevelMenus;
	}

	public void setTwoLevelMenus(List<SysMenuInfo> twoLevelMenus) {
		this.twoLevelMenus = twoLevelMenus;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public String getKlass() {
		return klass;
	}

	public void setKlass(String klass) {
		this.klass = klass;
	}

	public String getActive() {
		return active;
	}

	public void setActive(String active) {
		this.active = active;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}
}
