package com.zssq.news.model;

import java.io.Serializable;

/**
 * @author SharlaCheung
 * @ClassName: NewsCollectModel
 * @Description: 新闻收藏增删改参数类
 * @date 2017年04月11日
 */
public class NewsTransmitModel implements Serializable {
    private static final long serialVersionUID = -1606166264275829882L;

    private String userCode ;
    private String token ;
    private String newsCode ;
    private String tenantCode ;
    private String orgCode;
    private int orgLevel;
    private String remark;
    private String TransmitCode;

    public String getUserCode() {
        return userCode;
    }

    public void setUserCode(String userCode) {
        this.userCode = userCode;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getNewsCode() {
        return newsCode;
    }

    public void setNewsCode(String newsCode) {
        this.newsCode = newsCode;
    }

    public String getTenantCode() {
        return tenantCode;
    }

    public void setTenantCode(String tenantCode) {
        this.tenantCode = tenantCode;
    }

    public String getOrgCode() {
        return orgCode;
    }

    public void setOrgCode(String orgCode) {
        this.orgCode = orgCode;
    }

    public int getOrgLevel() {
        return orgLevel;
    }

    public void setOrgLevel(int orgLevel) {
        this.orgLevel = orgLevel;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getTransmitCode() {
        return TransmitCode;
    }

    public void setTransmitCode(String transmitCode) {
        TransmitCode = transmitCode;
    }
}
