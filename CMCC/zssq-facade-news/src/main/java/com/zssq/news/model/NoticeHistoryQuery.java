package com.zssq.news.model;


import java.io.Serializable;

/**
 * @author SharlaCheung
 * @ClassName: NoticeHistoryQuery
 * @Description: 新闻历程查询类
 * @date 2017年04月02日
 */
public class NoticeHistoryQuery implements Serializable{

    private static final long serialVersionUID = -1606166264275829881L;

    private String noticeCode ;
    private String token ;
    private String userCode; //查询用户code

    public String getNoticeCode() {
        return noticeCode;
    }

    public void setNoticeCode(String noticeCode) {
        this.noticeCode = noticeCode;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getUserCode() {
        return userCode;
    }

    public void setUserCode(String userCode) {
        this.userCode = userCode;
    }
}
