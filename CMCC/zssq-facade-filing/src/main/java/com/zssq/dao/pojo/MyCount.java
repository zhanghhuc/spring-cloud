package com.zssq.dao.pojo;

import java.io.Serializable;

public class MyCount implements Serializable {

	private static final long serialVersionUID = 1L;

	private Integer friendsCount;
	
	private Integer fansCount;
	
	private Integer concernsCount;
	
	private Integer blackCount;

	public Integer getFriendsCount() {
		return friendsCount;
	}

	public void setFriendsCount(Integer friendsCount) {
		this.friendsCount = friendsCount;
	}

	public Integer getFansCount() {
		return fansCount;
	}

	public void setFansCount(Integer fansCount) {
		this.fansCount = fansCount;
	}

	public Integer getConcernsCount() {
		return concernsCount;
	}

	public void setConcernsCount(Integer concernsCount) {
		this.concernsCount = concernsCount;
	}

	public Integer getBlackCount() {
		return blackCount;
	}

	public void setBlackCount(Integer blackCount) {
		this.blackCount = blackCount;
	}
	
}
