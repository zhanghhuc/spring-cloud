package com.zssq.dao.pojo;

import com.zssq.pojo.BasePage;

public class SensitiveWordInfo extends BasePage{
	
	private static final long serialVersionUID = 1L;

	//自增主键
	private Long id;
	
	//敏感词编号
	private String sensitiveCode;
	
	//敏感词内容
	private String sensitiveValue;
	
	//敏感词拼音
	private String sensitiveSpellValue;
	
	//敏感词缩写
	private String sensitiveAcronym;
	
	//创建时间
	private Long createTime;
	
	//0:未启用; 1:启用
	private Byte isEnable;
	
	//敏感词所属组织机构编号，基础敏感词存 0
	private String orgCode;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getSensitiveCode() {
		return sensitiveCode;
	}

	public void setSensitiveCode(String sensitiveCode) {
		this.sensitiveCode = sensitiveCode;
	}

	public String getSensitiveValue() {
		return sensitiveValue;
	}

	public void setSensitiveValue(String sensitiveValue) {
		this.sensitiveValue = sensitiveValue;
	}

	public String getSensitiveSpellValue() {
		return sensitiveSpellValue;
	}

	public void setSensitiveSpellValue(String sensitiveSpellValue) {
		this.sensitiveSpellValue = sensitiveSpellValue;
	}

	public String getSensitiveAcronym() {
		return sensitiveAcronym;
	}

	public void setSensitiveAcronym(String sensitiveAcronym) {
		this.sensitiveAcronym = sensitiveAcronym;
	}

	public Long getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Long createTime) {
		this.createTime = createTime;
	}

	public Byte getIsEnable() {
		return isEnable;
	}

	public void setIsEnable(Byte isEnable) {
		this.isEnable = isEnable;
	}

	public String getOrgCode() {
		return orgCode;
	}

	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}

}
