package com.zssq.vo;

import java.io.Serializable;
/**
 * 
 * @ClassName: RepositoryInfoVo  
 * @Description: 知识库vo  
 * @author sry  
 * @date 2017年4月28日  
 *
 */
public class RepositoryInfoVo implements Serializable{

	private static final long serialVersionUID = 6502816347400395094L;
	
	private String orgCode;//组织编号
	
	private Long queryTime;//查询时间
	
	private String userCode;//用户编号
	
	private String repositoryCode;//知识库编号
	



	public String getRepositoryCode() {
		return repositoryCode;
	}

	public void setRepositoryCode(String repositoryCode) {
		this.repositoryCode = repositoryCode;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}

	public String getOrgCode() {
		return orgCode;
	}

	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}

	public Long getQueryTime() {
		return queryTime;
	}

	public void setQueryTime(Long queryTime) {
		this.queryTime = queryTime;
	}

	public String getUserCode() {
		return userCode;
	}

	public void setUserCode(String userCode) {
		this.userCode = userCode;
	}
	
	
}
