package com.zssq.vo;

import java.io.Serializable;

/**
 * 
 * @ClassName: RelationSubjectVO
 * @Description: 内容信息
 * @author ZKZ
 * @date 2017年3月18日
 *
 */
public class RelationSubjectVO implements Serializable {

	private static final long serialVersionUID = 1L;

	private String subjectCode; // 内容编号
	private Byte subjectClass; // 内容类型：1.微博；2.博客；3.投票；4.活动；5.新闻公告
	private String subjectTitle; // 标题
	private String subjectDigest; // 摘要
	private String subjectUrl; // 图片链接
	private Byte actionClass; // 操作类型：1.屏蔽/隐藏；2.恢复/取消隐藏
	private Long modifyTime;// 修改时间

	public String getSubjectCode() {
		return subjectCode;
	}

	public void setSubjectCode(String subjectCode) {
		this.subjectCode = subjectCode;
	}

	public Byte getSubjectClass() {
		return subjectClass;
	}

	public void setSubjectClass(Byte subjectClass) {
		this.subjectClass = subjectClass;
	}

	public String getSubjectTitle() {
		return subjectTitle;
	}

	public void setSubjectTitle(String subjectTitle) {
		this.subjectTitle = subjectTitle;
	}

	public String getSubjectDigest() {
		return subjectDigest;
	}

	public void setSubjectDigest(String subjectDigest) {
		this.subjectDigest = subjectDigest;
	}

	public Byte getActionClass() {
		return actionClass;
	}

	public void setActionClass(Byte actionClass) {
		this.actionClass = actionClass;
	}

	public Long getModifyTime() {
		return modifyTime;
	}

	public void setModifyTime(Long modifyTime) {
		this.modifyTime = modifyTime;
	}

	public String getSubjectUrl() {
		return subjectUrl;
	}

	public void setSubjectUrl(String subjectUrl) {
		this.subjectUrl = subjectUrl;
	}

}
