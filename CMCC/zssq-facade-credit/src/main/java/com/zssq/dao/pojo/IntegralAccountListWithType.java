package com.zssq.dao.pojo;

import java.util.List;

import com.zssq.pojo.BasePage;

/**
 * 
 * @ClassName: IntegralAccount  
 * @Description: 个人或班组的积分账户查询列表实体  
 * @author power  
 * @date 2017年4月27日  
 *
 */
public class IntegralAccountListWithType extends BasePage {

	private static final long serialVersionUID = 1L;
	
	/** 积分账户类型：1-个人，2-班组 */
	private String accountType;
	
	/** 所属行政组织编号集合 */
	private List<String> orgCodes;
	
	/** 积分账户编号 */
	private String accountCode;
	
	/** 从个人或班组信息服务的模糊查询中得到的积分账户编号集合 */
	private List<String> accountCodes;
	
	public String getAccountType() {
		return accountType;
	}

	public void setAccountType(String accountType) {
		this.accountType = accountType;
	}

	public List<String> getOrgCodes() {
		return orgCodes;
	}

	public void setOrgCodes(List<String> orgCodes) {
		this.orgCodes = orgCodes;
	}

	public String getAccountCode() {
		return accountCode;
	}

	public void setAccountCode(String accountCode) {
		this.accountCode = accountCode;
	}

	public List<String> getAccountCodes() {
		return accountCodes;
	}

	public void setAccountCodes(List<String> accountCodes) {
		this.accountCodes = accountCodes;
	}
	
}
