package com.zssq.blog.vo;

import java.io.Serializable;
import java.sql.Timestamp;

/**
 * 
 * @ClassName: DB2BlogCatalog
 * @Description: 博客分类
 * @author ZKZ
 * @date 2017年5月19日
 *
 */
public class DB2BlogCatalog implements Serializable {

	private static final long serialVersionUID = 1L;

	private Integer cataId;
	private String parentsCataName;
	private String cataName;
	private Timestamp createTime;
	

	public Integer getCataId() {
		return cataId;
	}

	public void setCataId(Integer cataId) {
		this.cataId = cataId;
	}

	public String getParentsCataName() {
		return parentsCataName;
	}

	public void setParentsCataName(String parentsCataName) {
		this.parentsCataName = parentsCataName;
	}

	public String getCataName() {
		return cataName;
	}

	public void setCataName(String cataName) {
		this.cataName = cataName;
	}

	public Timestamp getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Timestamp createTime) {
		this.createTime = createTime;
	}

}
