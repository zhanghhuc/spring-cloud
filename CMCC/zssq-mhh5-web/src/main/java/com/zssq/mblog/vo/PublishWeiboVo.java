package com.zssq.mblog.vo;

import org.hibernate.validator.constraints.NotBlank;

/**
 * 
    * @ClassName: PublishWeiboVo  
    * @Description: 发微博条件VO  
    * @author Mr.B  
    * @date 2017年3月22日  
    *
 */
public class PublishWeiboVo {

	@NotBlank(message = "{empty.message}")
	private String userCode; // 用户CODE
	@NotBlank(message = "{empty.message}")
	private String token; // token
	@NotBlank(message = "{empty.message}")
	private String content; // 内容
	@NotBlank(message = "{empty.message}")
	private String textContent; // 文本内容
	@NotBlank(message = "{empty.message}")
	private String summary; // 摘要
	
	private String imgs = ""; // 图片链接
	private String audios = ""; // 音频链接
	private String videos = ""; // 视频链接
	private String topicCodes = ""; // 话题链接
	private String atUserCodes = ""; // AT用户列表	
	
	public String getUserCode() {
		return userCode;
	}
	public void setUserCode(String userCode) {
		this.userCode = userCode;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getTextContent() {
		return textContent;
	}
	public void setTextContent(String textContent) {
		this.textContent = textContent;
	}
	public String getSummary() {
		return summary;
	}
	public void setSummary(String summary) {
		this.summary = summary;
	}
	public String getImgs() {
		return imgs;
	}
	public void setImgs(String imgs) {
		this.imgs = imgs;
	}
	public String getAudios() {
		return audios;
	}
	public void setAudios(String audios) {
		this.audios = audios;
	}
	public String getVideos() {
		return videos;
	}
	public void setVideos(String videos) {
		this.videos = videos;
	}
	public String getTopicCodes() {
		return topicCodes;
	}
	public void setTopicCodes(String topicCodes) {
		this.topicCodes = topicCodes;
	}
	public String getAtUserCodes() {
		return atUserCodes;
	}
	public void setAtUserCodes(String atUserCodes) {
		this.atUserCodes = atUserCodes;
	}
	
	
	
}
