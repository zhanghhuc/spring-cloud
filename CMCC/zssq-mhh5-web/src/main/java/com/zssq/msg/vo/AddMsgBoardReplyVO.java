package com.zssq.msg.vo;

import org.hibernate.validator.constraints.NotBlank;

public class AddMsgBoardReplyVO {

	@NotBlank(message = "{empty.message}")
	//收信人
	private String beReplyUserCode;
	@NotBlank(message = "{empty.message}")
	private String content;
	@NotBlank(message = "{empty.message}")
	private String messageBoardCode;
	@NotBlank(message = "{empty.message}")
	//发送人
	private String userCode;
	@NotBlank(message = "{empty.message}")
	private String orgContent;
	

	public String getOrgContent() {
		return orgContent;
	}

	public void setOrgContent(String orgContent) {
		this.orgContent = orgContent;
	}

	public String getUserCode() {
		return userCode;
	}

	public void setUserCode(String userCode) {
		this.userCode = userCode;
	}

	public String getBeReplyUserCode() {
		return beReplyUserCode;
	}

	public void setBeReplyUserCode(String beReplyUserCode) {
		this.beReplyUserCode = beReplyUserCode;
	}


	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}
	

	public String getMessageBoardCode() {
		return messageBoardCode;
	}

	public void setMessageBoardCode(String messageBoardCode) {
		this.messageBoardCode = messageBoardCode;
	}

	
	
}
                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                 