package com.zssq.vo;

import java.io.Serializable;

/**
 * 
 * @ClassName: BlogSubVO
 * @Description: 订阅参数
 * @author ZKZ
 * @date 2017年3月27日
 *
 */
public class BlogSubVO implements Serializable {

	private static final long serialVersionUID = 1L;

	private String userCode; // 订阅人编号
	private Byte subClass; // 被订阅类型：1.个人；2.班组
	private String teamCode; // 被订阅班组编号
	private String subUserCode; // 被订阅人编号

	
	public String getUserCode() {
		return userCode;
	}

	public void setUserCode(String userCode) {
		this.userCode = userCode;
	}

	public Byte getSubClass() {
		return subClass;
	}

	public void setSubClass(Byte subClass) {
		this.subClass = subClass;
	}

	public String getTeamCode() {
		return teamCode;
	}

	public void setTeamCode(String teamCode) {
		this.teamCode = teamCode;
	}

	public String getSubUserCode() {
		return subUserCode;
	}

	public void setSubUserCode(String subUserCode) {
		this.subUserCode = subUserCode;
	}

}
