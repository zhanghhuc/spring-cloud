package com.zssq.dao.model;

import java.io.Serializable;

/**
 * 
 * @ClassName: BlogReplyModel  
 * @Description: 博客评论回复返回参数  
 * @author ZKZ  
 * @date 2017年3月24日  
 *
 */
public class BlogReplyModel implements Serializable {

	private static final long serialVersionUID = 1L;
	
	private String blogCode; // 博客编号
	private String commentCode; // 评论编号
	private String replyCode; // 回复编号
    private Long createTime; // 创建时间
    private String userCode; // 人员编号
    private String toReplyUserCode; // 被回复人编号
    private Integer replyLikeNum; // 点赞量
    private String replyContent; // 回复内容
	private Byte isLike; // 是否点赞
	

	public String getBlogCode() {
		return blogCode;
	}

	public void setBlogCode(String blogCode) {
		this.blogCode = blogCode;
	}

	public String getCommentCode() {
		return commentCode;
	}

	public void setCommentCode(String commentCode) {
		this.commentCode = commentCode;
	}

	public String getReplyCode() {
		return replyCode;
	}

	public void setReplyCode(String replyCode) {
		this.replyCode = replyCode;
	}

	public Long getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Long createTime) {
		this.createTime = createTime;
	}

	public String getUserCode() {
		return userCode;
	}

	public void setUserCode(String userCode) {
		this.userCode = userCode;
	}

	public String getToReplyUserCode() {
		return toReplyUserCode;
	}

	public void setToReplyUserCode(String toReplyUserCode) {
		this.toReplyUserCode = toReplyUserCode;
	}

	public Integer getReplyLikeNum() {
		return replyLikeNum;
	}

	public void setReplyLikeNum(Integer replyLikeNum) {
		this.replyLikeNum = replyLikeNum;
	}

	public String getReplyContent() {
		return replyContent;
	}

	public void setReplyContent(String replyContent) {
		this.replyContent = replyContent;
	}

	public Byte getIsLike() {
		return isLike;
	}

	public void setIsLike(Byte isLike) {
		this.isLike = isLike;
	}

}
