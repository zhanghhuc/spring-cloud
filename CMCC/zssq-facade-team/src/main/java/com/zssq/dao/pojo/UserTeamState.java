package com.zssq.dao.pojo;

import java.io.Serializable;

/**
 * @ClassName UserTeamState
 * @Description 用户与班组关系  是否关注该班组/该班组是否和我是班组长的所有班组是友好班组
 * @author JiPengChun
 * @date 2017年6月7日 下午2:54:29
 * @version 1.0
 * @since JDK 1.7
 */
public class UserTeamState implements Serializable{

	private static final long serialVersionUID = 1L;

	/** 是否关注该班组 */
	private Byte isConcern;
	
	/** 该班组是否和我是班组长的所有班组是友好班组 */
	private Byte isFriendTeam;

	public Byte getIsConcern() {
		return isConcern;
	}

	public void setIsConcern(Byte isConcern) {
		this.isConcern = isConcern;
	}

	public Byte getIsFriendTeam() {
		return isFriendTeam;
	}

	public void setIsFriendTeam(Byte isFriendTeam) {
		this.isFriendTeam = isFriendTeam;
	}
	
}
