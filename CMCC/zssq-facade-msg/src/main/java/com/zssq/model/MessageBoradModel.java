package com.zssq.model;

import java.io.Serializable;

public class MessageBoradModel implements Serializable{
	
	private String userCode;
	
	private int pageSize;
	
	private int pageNo;
	//查询类型 ：1我收到的留言，2我发出的留言
	private int type; 
	//回复列表pageSize
	private int replyPageSize;
	//回复列表pageNo;
	private int replyPageNo;
	
	

	public int getReplyPageSize() {
		return replyPageSize;
	}

	public void setReplyPageSize(int replyPageSize) {
		this.replyPageSize = replyPageSize;
	}

	public int getReplyPageNo() {
		return replyPageNo;
	}

	public void setReplyPageNo(int replyPageNo) {
		this.replyPageNo = replyPageNo;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getUserCode() {
		return userCode;
	}

	public void setUserCode(String userCode) {
		this.userCode = userCode;
	}

	public int getPageSize() {
		return pageSize;
	}

	public void setPageSize(int pageSize) {
		this.pageSize = pageSize;
	}

	public int getPageNo() {
		return pageNo;
	}

	public void setPageNo(int pageNo) {
		this.pageNo = pageNo;
	}
	
}
