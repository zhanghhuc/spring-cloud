package com.zssq.dao.pojo;

import java.io.Serializable;
import java.util.List;

/**
 * 参与投票时传递参数的实体
 * @author POWER
 *
 */
public class JoinVoteEntity implements Serializable {
	private static final long serialVersionUID = -5318897445047022025L;
	//投票信息code
	private String voteInfoCode;
	//选项集合
	private String optionsCode;
	//选项列表集合
	private List<Integer> optionsCodeList;
	//用户code
	private String userCode;
	//租户code
	private String tenantCode;
	//组织机构code
	private String orgCode;
	//组织机构等级
	private Byte orgLevel;
	
	public List<Integer> getOptionsCodeList() {
		return optionsCodeList;
	}
	public void setOptionsCodeList(List<Integer> optionsCodeList) {
		this.optionsCodeList = optionsCodeList;
	}
	public String getVoteInfoCode() {
		return voteInfoCode;
	}
	public void setVoteInfoCode(String voteInfoCode) {
		this.voteInfoCode = voteInfoCode;
	}
	public String getOptionsCode() {
		return optionsCode;
	}
	public void setOptionsCode(String optionsCode) {
		this.optionsCode = optionsCode;
	}
	public String getUserCode() {
		return userCode;
	}
	public void setUserCode(String userCode) {
		this.userCode = userCode;
	}
	public String getTenantCode() {
		return tenantCode;
	}
	public void setTenantCode(String tenantCode) {
		this.tenantCode = tenantCode;
	}
	public String getOrgCode() {
		return orgCode;
	}
	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}
	public Byte getOrgLevel() {
		return orgLevel;
	}
	public void setOrgLevel(Byte orgLevel) {
		this.orgLevel = orgLevel;
	}
}
